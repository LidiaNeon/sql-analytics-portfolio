-- Нужно понять, как меняется выручка по товарам из месяца в месяц.
-- Какие вопросы решает кейс:
-- 1. Посчитать помесячную выручку по каждому товару.
-- 2. Посчитать процентное изменение выручки относительно прошлого месяца (MoM).
-- 3. Выделить товары, у которых резкое падение/рост (например, больше +50% или меньше −30%).
